fun main() {
    
    println("Enter a number between 1 and 10.")
    var x = Integer.valueOf(readLine())
    
    when (x) {
    1 -> print("I")
    2 -> print("II")
    3 -> print("III")
    4 -> print("VI")
    5 -> print("V")
    6 -> print("IV")
    7 -> print("VII")
    8 -> print("VIII")
    9 -> print("IX")
    10 -> print("X")
    else -> {
        print("error")
}
}
